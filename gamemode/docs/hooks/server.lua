--[[
    Lilia Framework - Server-Side Hooks

    Server-side hook system for the Lilia framework.
    These hooks run on the server and are used for game logic, data management, and server-side processing.
]]
--[[
    Overview:
    Server-side hooks in the Lilia framework handle game logic, data persistence, 
    player management, and other server-specific functionality. These hooks are executed 
    on the server and can be used to customize game mechanics, handle player actions, 
    manage data, and control server behavior. All server hooks follow the standard 
    Garry's Mod hook system and can be overridden or extended by addons and modules.
]]