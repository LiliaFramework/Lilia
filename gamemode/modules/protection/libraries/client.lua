local genericSuspiciousHookNeedles = {"aim", "esp", "wall", "hack", "cheat", "bhop", "trigger", "nospread", "norecoil", "cham", "xray"}
local function VerifyCheats()
    local function flag()
        net.Start("liaCheckHack")
        net.SendToServer()
    end

    for func in pairs(suspiciousFunctions) do
        local f = _G[func]
        if f and (debug.getinfo(f, "S") or {}).what ~= "C" then return flag() end
    end

    local hackCommands = concommand.GetTable()
    for command in pairs(HackCommands) do
        if hackCommands[command] then return flag() end
    end

    local hookTable = hook.GetTable()
    for hookName, suspiciousList in pairs(suspiciousHooks) do
        local hooks = hookTable[hookName]
        if hooks then
            for i = 1, #suspiciousList do
                if hooks[suspiciousList[i]] then return flag() end
            end
        end
    end

    for _, hooks in pairs(hookTable) do
        for hookFuncName in pairs(hooks) do
            local hookNameString = isstring(hookFuncName) and hookFuncName or tostring(hookFuncName)
            local lowered = isstring(hookNameString) and hookNameString:lower() or ""
            if lowered ~= "" then
                local found = false
                for i = 1, #genericSuspiciousHookNeedles do
                    if lowered:find(genericSuspiciousHookNeedles[i], 1, true) then
                        found = true
                        break
                    end
                end

                if found or (lowered:find("menu", 1, true) and lowered:find("hack", 1, true)) or lowered:match("^%d+$") or lowered:match("^[%+%-%=]") then return flag() end
            end
        end
    end

    for filename in pairs(suspiciousFiles) do
        if file.Exists(filename, "DATA") then return flag() end
    end

    for dirname in pairs(suspiciousDirs) do
        if file.IsDir(dirname, "DATA") or file.IsDir(dirname, "GAME") or file.IsDir("lua/" .. dirname, "GAME") then return flag() end
    end

    for cvar in pairs(BadCVars) do
        if ConVarExists(cvar) then return flag() end
    end

    for globalName in pairs(HackGlobals) do
        if _G[globalName] then return flag() end
    end

    if detect_oink() or detect_cheadleware() then return flag() end
end

function MODULE:PlayerButtonDown(client, key)
    if TRIGGER_KEYS[key] then
        timer.Remove("clipboard_blocker")
        local endAt = CurTime() + 30
        SetClipboardText("")
        timer.Create("clipboard_blocker", 0.4, 0, function()
            if CurTime() >= endAt then
                timer.Remove("clipboard_blocker")
                return
            end

            SetClipboardText("")
        end)
    end

    if key == KEY_INSERT and IsFirstTimePredicted() then
        local ply = LocalPlayer()
        if IsValid(ply) and ply == client then
            net.Start("liaInsertKeyPressed")
            net.SendToServer()
        end
    end
end

function MODULE:InitPostEntity()
